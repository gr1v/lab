def binary_search(arr, target):
    """
    Реализация бинарного поиска в отсортированном массиве.
    
    Параметры:
    arr -- отсортированный список чисел
    target -- искомое значение
    
    Возвращает:
    индекс искомого элемента или -1, если элемент не найден
    """
    low = 0                 # нижний индекс диапазона поиска
    high = len(arr) - 1     # верхний индекс диапазона поиска
    
    while low <= high:              # пока диапазон поиска не исчерпался
        mid = (low + high) // 2    # находим середину диапазона
        
        if arr[mid] == target:      # если нашли элемент
            return mid              # возвращаем его индекс
        
        elif arr[mid] < target:     # если середина меньше искомого
            low = mid + 1           # сдвигаемся вправо
        
        else:                       # иначе, если середина больше искомого
            high = mid - 1          # сдвигаемся влево
    
    return -1                      # если элемент не найден, возвращаем -1


# Пример использования
if __name__ == "__main__":
    data = [1, 3, 5, 7, 9, 11, 13, 15]
    target = 7
    result = binary_search(data, target)
    
    if result != -1:
        print(f'Элемент {target} найден на позиции {result}.')
    else:
        print(f'Элемент {target} не найден.')